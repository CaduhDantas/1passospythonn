import math
an = float(input('escreva um angulo'))
sen = math.sin(math.radians(an))
cos = math.cos(math.radians(an))
tg = math.tan(math.radians(an))
print('o angulo {}, tem o seno de {:.2f}, cosseno de {:.2f} e a tangente de {:.2f}'.format(an, sen, cos, tg))
